# String Formatting: Using format(), f-strings, and %

name = "John"
age = 25

# Using format()
print("My name is {} and I am {} years old.".format(name, age))

# Using f-strings
print(f"My name is {name} and I am {age} years old.")

# Using % formatting
print("My name is %s and I am %d years old." % (name, age))

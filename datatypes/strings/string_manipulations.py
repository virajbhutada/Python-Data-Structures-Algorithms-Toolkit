# String Manipulations: Concatenation, Repetition, Case Conversion

# Concatenating strings
greeting = "Hello" + " " + "World!"
print("Concatenated string:", greeting)  # Output: Hello World!

# Repeating strings
repeat_str = "Hi! " * 3
print("Repeated string:", repeat_str)  # Output: Hi! Hi! Hi!

# Case conversion
print("Uppercase:", greeting.upper())  # Output: HELLO WORLD!
print("Lowercase:", greeting.lower())  # Output: hello world!

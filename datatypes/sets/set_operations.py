# Set Operations: Union, Intersection, and Difference

set1 = {1, 2, 3}
set2 = {3, 4, 5}

# Union of sets
print("Union:", set1 | set2)  # Output: {1, 2, 3, 4, 5}

# Intersection of sets
print("Intersection:", set1 & set2)  # Output: {3}

# Difference of sets
print("Difference (set1 - set2):", set1 - set2)  # Output: {1, 2}

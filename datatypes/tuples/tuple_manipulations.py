# Tuple Manipulations: Slicing, Indexing, and Accessing Elements

my_tuple = (1, 2, 3, 4, 5)

# Accessing elements by index
print("Element at index 1:", my_tuple[1])  # Output: 2

# Slicing tuples
print("Sliced tuple (index 1 to 3):", my_tuple[1:3])  # Output: (2, 3)

# Negative indexing
print("Last element:", my_tuple[-1])  # Output: 5
